rd1 = vtk.vtkXMLUnstructuredGridReader()
rd1.SetFileName(vtufilename)
rd1.Update()
whole_mesh = rd1.GetOutput()

#material
filename = '{:s}-LMATE.mpio.bin'.format(problem_name)
material = vtk_to_numpy(whole_mesh.GetCellData().GetArray(material_array))[:,np.newaxis]
write_lmate_mpio( filename, material )

for field_id in range(xfiel_max):
   filename = '{:s}-XFIEL.{:08d}.{:08d}.mpio.bin'.format(problem_name, field_id, 1)
   arrayname = xfiel_array.format(field_id)
   a = whole_mesh.GetPointData().GetArray(arrayname)
   if a:
      aa = vtk_to_numpy(a)
      if aa.ndim == 1:
         write_xfiel_mpio( filename, aa[:,np.newaxis], 0 )
      else:
         write_xfiel_mpio( filename, aa, 0 )


